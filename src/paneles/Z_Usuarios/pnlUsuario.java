    private void buscarKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_buscarKeyTyped
        char letras = evt.getKeyChar();

        if (Character.isLowerCase(letras)) {
            String cad = ("" + letras).toUpperCase();
            letras = cad.charAt(0);
            evt.setKeyChar(letras);
        }
    }//GEN-LAST:event_buscarKeyTyped

    private void pnlremoverMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pnlremoverMouseClicked
        if (this.tabla.getRowCount() < 1) {
            WarningAlert er = new WarningAlert(new JFrame(), true);
            er.titulo.setText("Mensaje");
            er.msj.setText("La tabla esta ");
            er.msj1.setText("vacÃ­a");
            er.setVisible(true);
        } else {
            if (this.tabla.getSelectedRowCount() < 1) {
                WarningAlert er = new WarningAlert(new JFrame(), true);
                er.titulo.setText("Mensaje");
                er.msj.setText("Seleccione almenos un");
                er.msj1.setText("registro");
                er.setVisible(true);
            } else {
                int fila = this.tabla.getSelectedRow();

                if (this.tabla.getValueAt(fila, 0).toString().equals("1")) {
                    WarningAlert er = new WarningAlert(new JFrame(), true);
                    er.titulo.setText("Mensaje");
                    er.msj.setText("No tiene permitido eliminar");
                    er.msj1.setText("este usuario");
                    er.setVisible(true);
                } else {
                    WarningAlertU wa = new WarningAlertU(new JFrame(), true);
                    wa.id.setText(this.tabla.getValueAt(fila, 0).toString());
                    wa.titulo.setText("Â¿Esta Seguro?");
                    wa.msj.setText("Se borrara permanentemente");
                    wa.msj1.setText("un registro");
                    wa.setVisible(true);
                }
            }
        }
    }//GEN-LAST:event_pnlremoverMouseClicked

    private void pnlmodificarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pnlmodificarMouseClicked
        if (this.tabla.getRowCount() < 1) {
            WarningAlert er = new WarningAlert(new JFrame(), true);
            er.titulo.setText("Mensaje");
            er.msj.setText("La tabla actualmente");
            er.msj1.setText("esta vacia");
            er.setVisible(true);
        } else {
            if (this.tabla.getSelectedRowCount() < 1) {
                WarningAlert er = new WarningAlert(new JFrame(), true);
                er.titulo.setText("Mensaje");
                er.msj.setText("Seleccione al menos un");
                er.msj1.setText("Registro");
                er.setVisible(true);
            } else {

                int fila = this.tabla.getSelectedRow();
                
                Usuario mp = new Usuario(new JFrame(), true);
                mp.id.setText(this.tabla.getValueAt(fila, 0).toString());
                mp.txtNombre.setText(this.tabla.getValueAt(fila, 1).toString());
                mp.txtApellido_P.setText(this.tabla.getValueAt(fila, 2).toString());
                mp.txtApellido_M.setText(this.tabla.getValueAt(fila, 3).toString());
                mp.txtDireccion.setText(this.tabla.getValueAt(fila, 4).toString());
                mp.txtNumero.setText(this.tabla.getValueAt(fila, 5).toString());
                mp.txtUsuario.setText(this.tabla.getValueAt(fila, 6).toString());
                mp.txtPassword.setText(this.tabla.getValueAt(fila, 7).toString());
                mp.tipo.setSelectedItem(this.tabla.getValueAt(fila, 8).toString());
                mp.titulo.setText("Modificar Usuarios");
                mp.registrar.setText("Guardar");
                mp.setVisible(true);
            }
        }
    }//GEN-LAST:event_pnlmodificarMouseClicked

